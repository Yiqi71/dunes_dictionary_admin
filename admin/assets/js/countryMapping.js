const COUNTRY_NAME_TO_ISO2 = (() => {
    const map = {};
    for (const [code, meta] of Object.entries(country_bounding_boxes)) {
        const name = meta?.[0];
        if (!name) continue;
        map[name.toLowerCase()] = code;
    }
    return map;
})();


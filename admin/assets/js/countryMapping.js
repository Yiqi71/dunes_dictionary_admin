function resolveCountryCode(raw) {
    const v = (raw || "").trim();
    if (!v) return "";
    const upper = v.toUpperCase();
    if (country_bounding_boxes[upper]) return upper;
    const alias = COUNTRY_ALIAS_TO_ISO2[v];
    if (alias) return alias;
    const byName = COUNTRY_NAME_TO_ISO2[v.toLowerCase()];
    return byName || v;
}

